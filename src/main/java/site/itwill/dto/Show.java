public class Show {
	private int showseq;
	private String showTime;
	private String title;
	private int theatre;
	private String shortSummary;
	private int showLength;
	private String posterPicture;
	private String team;
	private String playwright;
	private String rating;
	private String summary;
	
	public int getShowseq() {
		return showseq;
	}

	public void setShowseq(int showseq) {
		this.showseq = showseq;
	}
	
	public String getTeam() {
		return team;
	}

	public void setTeam(String team) {
		this.team = team;
	}

	public String getPlaywright() {
		return playwright;
	}

	public void setPlaywright(String playwright) {
		this.playwright = playwright;
	}

	public String getRating() {
		return rating;
	}

	public void setRating(String rating) {
		this.rating = rating;
	}

	public String getSummary() {
		return summary;
	}

	public void setSummary(String summary) {
		this.summary = summary;
	}

	public Show() {
		// TODO Auto-generated constructor stub
	}

	public String getShowTime() {
		return showTime;
	}

	public void setShowTime(String showTime) {
		this.showTime = showTime;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public int getTheatre() {
		return theatre;
	}

	public void setTheatre(int theatre) {
		this.theatre = theatre;
	}

	public String getShortSummary() {
		return shortSummary;
	}

	public void setShortSummary(String shortSummary) {
		this.shortSummary = shortSummary;
	}

	public int getShowLength() {
		return showLength;
	}

	public void setShowLength(int showLength) {
		this.showLength = showLength;
	}

	public String getPosterPicture() {
		return posterPicture;
	}

	public void setPosterPicture(String posterPicture) {
		this.posterPicture = posterPicture;
	}
}
